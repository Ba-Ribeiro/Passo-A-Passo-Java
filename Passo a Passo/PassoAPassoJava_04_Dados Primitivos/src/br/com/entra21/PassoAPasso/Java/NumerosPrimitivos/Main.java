package br.com.entra21.PassoAPasso.Java.NumerosPrimitivos;

public class Main {

	public static void main(String[] args) {
	// 8 tipos de dados primitivos em java
		
		byte a;  //-128 até 127
		short b; //-32768 até 32.767
		int c;   //-2147483648 até + 2147483647  
		long d;  // -922337203685477808 até 922337203685477807
		
		// a, b, c e d: são capazes de armazenar valores inteiros
		//negativos e positivos 
		
		double e; //variavel double vai permitir adicionar variaveis decimais e /tem uma precisão maior 
		
		float f; // a variavel float vai permitir adicionar variaveis decimais, logo precisa acrescentar a letra f após o numero. /tem uma precisão menor no resultado.
		
		// e e f são capazes de armazenar valores decimais
		
		boolean g;  // é capaz de armazenar basicamente 0 ou 1. 0 = false 1= true
		char h= '?'; //é capaz de armazenar caracteries 
		
	}

}
